// Normaliza para comparar sin acentos ni mayúsculas.
export function normalizeBreed(s: string): string {
  return s
    .toLowerCase()
    .normalize("NFD")
    .replace(/[̀-ͯ]/g, "") // quita acentos (marcas diacríticas)
    .trim();
}
